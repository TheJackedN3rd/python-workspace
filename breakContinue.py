for i in range(1,11):
    if i ==5:
        break
    print(i)
    
    
for i in range(1,11):
    if i ==5:
        continue
    print(i)
    
    
# This code snippet demonstrates the use of break and continue in loops.
# The 'break' statement exits the loop when i equals 5, while 'continue' skips the current iteration when i equals 5, allowing the loop to continue with the next iteration.

